export function categorizeIngredient(ingredient: string): string {
  const normalizedIngredient = ingredient.toLowerCase().trim();
  
  // Check each category for matches
  for (const [category, items] of Object.entries(INGREDIENT_CATEGORIES)) {
    for (const item of items) {
      if (normalizedIngredient.includes(item.toLowerCase()) || 
          item.toLowerCase().includes(normalizedIngredient)) {
        return category;
      }
    }
  }
  
  // Additional smart matching for common ingredient patterns
  if (normalizedIngredient.includes('chicken') || normalizedIngredient.includes('beef') || 
      normalizedIngredient.includes('pork') || normalizedIngredient.includes('lamb') ||
      normalizedIngredient.includes('turkey') || normalizedIngredient.includes('fish') ||
      normalizedIngredient.includes('salmon') || normalizedIngredient.includes('tuna')) {
    return 'Meat';
  }
  
  if (normalizedIngredient.includes('cheese') || normalizedIngredient.includes('milk') ||
      normalizedIngredient.includes('cream') || normalizedIngredient.includes('butter') ||
      normalizedIngredient.includes('yogurt') || normalizedIngredient.includes('yoghurt')) {
    return 'Dairy';
  }
  
  if (normalizedIngredient.includes('bread') || normalizedIngredient.includes('roll') ||
      normalizedIngredient.includes('bun') || normalizedIngredient.includes('pastry') ||
      normalizedIngredient.includes('croissant') || normalizedIngredient.includes('muffin')) {
    return 'Bakery';
  }
  
  if (normalizedIngredient.includes('frozen')) {
    return 'Frozen';
  }
  
  // Check for common fruits and vegetables
  const commonFruits = ['apple', 'banana', 'orange', 'lemon', 'lime', 'tomato', 'avocado', 'berry'];
  const commonVegetables = ['onion', 'garlic', 'carrot', 'potato', 'lettuce', 'spinach', 'broccoli', 'pepper'];
  
  if (commonFruits.some(fruit => normalizedIngredient.includes(fruit)) ||
      commonVegetables.some(veg => normalizedIngredient.includes(veg))) {
    return 'Vegetables and Fruits';
  }
  
  // Default to Pantry for everything else
  return 'Pantry';
}

export function organizeShoppingListByCategory(items: any[]): Record<string, any[]> {
  const categorized: Record<string, any[]> = {};
  
  items.forEach(item => {
    const category = categorizeIngredient(item.ingredient);
    if (!categorized[category]) {
      categorized[category] = [];
    }
    categorized[category].push(item);
  });
  
  // Sort categories in a logical shopping order
  const categoryOrder = ['Vegetables and Fruits', 'Meat', 'Dairy', 'Bakery', 'Frozen', 'Pantry'];
  const organized: Record<string, any[]> = {};
  
  categoryOrder.forEach(category => {
    if (categorized[category]) {
      organized[category] = categorized[category];
    }
  });
  
  // Add any remaining categories not in the predefined order
  Object.keys(categorized).forEach(category => {
    if (!categoryOrder.includes(category)) {
      organized[category] = categorized[category];
    }
  });
  
  return organized;
}